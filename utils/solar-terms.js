function getCalendarData(year) {
  // 返回该年24个节气的月日数据（简化版）
  const sTerms = [
    // [月, 日] × 24
    1,5, 1,20, 2,3, 2,18, 3,5, 3,20,
    4,4, 4,19, 5,5, 5,20, 6,5, 6,21,
    7,6, 7,22, 7,23, 8,7, 8,22, 8,23,
    9,7, 9,22, 9,23,10,8, 10,23, 11,7,
    11,22, 11,22, 12,6, 12,21, 12,21
  ];
  // 根据年份微调
  const adj = getTermAdjustments(year);
  if (adj.length > 0) {
    for (let i = 0; i < 24; i++) {
      if (adj[i] !== undefined) sTerms[i * 2 + 1] = adj[i];
    }
  }
  return { sTerms };
}

/**
 * 获取指定年份所有节气的日期
 */
function getYearSolarTerms(year) {
  const terms = [];
  for (let i = 0; i < 24; i++) {
    const date = getSolarTermDate(year, i);
    terms.push({
      name: solarTermNames[i],
      month: date.month,
      day: date.day,
      dateString: `${year}-${padZero(date.month)}-${padZero(date.day)}`,
      index: i
    });
  }
  return terms;
}

/**
 * 获取指定日期是否是节气，以及是哪个节气
 */
function getTodaySolarTerm(year, month, day) {
  const terms = getYearSolarTerms(year);
  for (const term of terms) {
    if (term.month === month && term.day === day) {
      return term;
    }
  }
  return null;
}

/**
 * 获取指定日期附近的节气
 */
function getNearbySolarTerms(year, month, day) {
  const terms = getYearSolarTerms(year);
  const todayStr = `${year}-${padZero(month)}-${padZero(day)}`;

  let prev = null;
  let next = null;

  for (let i = 0; i < terms.length; i++) {
    if (terms[i].dateString <= todayStr) {
      prev = terms[i];
    }
    if (terms[i].dateString > todayStr && !next) {
      next = terms[i];
      break;
    }
  }

  if (!next) {
    const nextYearTerms = getYearSolarTerms(year + 1);
    next = nextYearTerms[0];
  }

  return { prev, next };
}

/**
 * 简化的节气日期计算
 */
function getSolarTermDate(year, index) {
  const termBaseDates = [
    [1, 5, 1, 7],   [1, 20, 1, 21], [2, 3, 2, 5],   [2, 18, 2, 20],
    [3, 5, 3, 7],   [3, 20, 3, 22], [4, 4, 4, 6],   [4, 19, 4, 21],
    [5, 5, 5, 7],   [5, 20, 5, 22], [6, 5, 6, 7],   [6, 21, 6, 22],
    [7, 6, 7, 8],   [7, 22, 7, 24], [8, 7, 8, 9],   [8, 22, 8, 24],
    [9, 7, 9, 9],   [9, 22, 9, 24], [10, 8, 10, 9], [10, 23, 10, 24],
    [11, 7, 11, 8], [11, 22, 11, 23], [12, 6, 12, 8], [12, 21, 12, 23]
  ];

  const base = termBaseDates[index];
  let day = base[1];

  const adjustments = getTermAdjustments(year);
  if (adjustments[index]) {
    day = adjustments[index];
  }

  return { month: base[0], day: day };
}

/**
 * 年度节气日期修正表（2020-2030）
 */
function getTermAdjustments(year) {
  const adjustmentTable = {
    2026: [5, 20, 4, 18, 5, 20, 5, 20, 5, 21, 5, 21, 7, 22, 7, 23, 7, 23, 8, 23, 7, 22, 7, 21],
    2025: [5, 20, 3, 18, 5, 20, 4, 20, 5, 21, 5, 21, 7, 22, 7, 23, 7, 23, 8, 23, 7, 22, 6, 21],
    2027: [5, 20, 4, 18, 5, 20, 5, 20, 5, 21, 5, 21, 7, 22, 7, 23, 7, 23, 8, 23, 7, 22, 7, 21],
    2024: [6, 20, 4, 19, 5, 20, 4, 20, 5, 20, 5, 21, 6, 22, 7, 22, 7, 23, 7, 23, 7, 22, 6, 21],
  };

  if (adjustmentTable[year]) {
    return adjustmentTable[year].reduce((acc, d, idx) => {
      acc[idx] = d;
      return acc;
    }, {});
  }

  return {};
}

function padZero(n) {
  return n < 10 ? '0' + n : '' + n;
}

